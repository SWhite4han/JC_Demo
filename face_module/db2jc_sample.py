import os
import json
import config
import numpy as np


db_name = config.db_name
code_path = os.path.dirname(os.path.abspath(__file__))

# Read data
face_vectors = np.load(os.path.join(code_path, db_name, 'face_vectors2.npy'))
face_source = np.load(os.path.join(code_path, db_name, 'face_source2.npy'))
face_locations = np.load(os.path.join(code_path, db_name, 'face_locations2.npy'))


def replace_path(old_path, key_dir_name, new_path_root):
    return old_path.replace(old_path[:old_path.index(key_dir_name)], new_path_root)

# generate sample data
for path, vec in list(zip(face_source, face_vectors)):
    sp = path.split('/')
    file_type = sp[-1].split('.')[-1]
    name = sp[-2] + '_' + sp[-1].replace(file_type, 'json')
    path = replace_path(path, 'known_people_id', 'img/')
    with open('/data1/Dslab_News/JC_Sample/sample_data_include_face/' + name, 'w') as wf:
        raw = {'category': 'face',
               'imgPath': path,
               'imgVec': vec.tolist()}
        json.dump(raw, wf)

